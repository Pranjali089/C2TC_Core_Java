public class if_else3 {
	public static void main(String[] args) {
		
		 int x = 10; int y = 11;
		  
		  if(!(x < y) || (x == y))
		  {  
			  System.out.println("Condition is TRUE");
		  }
		  else
		  { 
			  System.out.println("Condition is FALSE"); 
		}
		}

}
